    private void findUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findUserButtonActionPerformed
        /*
     lastLoginLabel;
     lastnameLabel;
     nameLabel;
     userUsernameLabel;
     countryLabel;
     emailLabel;
       TypeLabel
        */
        
        int userID = Integer.parseInt(UserIDTF.getText());
        User user = UserDAO.find(userID);
        userUsernameLabel.setText(user.getUsername());
         nameLabel.setText(user.getName());
         lastnameLabel.setText(user.getLast_name());
         emailLabel.setText(user.getEmail());
         TypeLabel.setText(user.getType());
         countryLabel.setText(Integer.toString(user.getCountry_id()));
        Date date=user.getLast_login();
         DateFormat dateFormat = new SimpleDateFormat("yyyy-mm-dd hh:mm:ss");  
         String strDate = dateFormat.format(date);  
         lastLoginLabel.setText(strDate);
    }//GEN-LAST:event_findUserButtonActionPerformed

