    private void saveUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUpdateButtonActionPerformed
        int plateID = Integer.parseInt(plateIDUpdateTF.getText());
        Plate plate = PlateDAO.find(plateID);
        String plateName=plateNameUpdateTF.getText();
        String plateDescription=plateDescriptionUpdateTF.getText();
        String plateRecipee=plateRecipeeUpdateTA.getText();
        boolean publicPlate=false;
        if (PublicRadioButtonYes.isSelected()){publicPlate=true;}
        plate.setPlateID(plateID);
        plate.setPlateName(plateName);
        plate.setPlateDescription(plateDescription);
        plate.setPlateRecipee(plateRecipee);
        plate.setPlatePublic(publicPlate);
        if(PlateDAO.update(plate)>0){
           this.setVisible(false);
        new fitbyte_appGUI().setVisible(true);
        }
    }//GEN-LAST:event_saveUpdateButtonActionPerformed

