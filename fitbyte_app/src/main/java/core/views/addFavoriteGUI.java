    private void saveFavoriteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFavoriteButtonActionPerformed
       Favorite favorite = new Favorite();
        int id_favorite=1;
        
        java.sql.Date date = new java.sql.Date(Calendar.getInstance().getTime().getTime());
        int user_id =current_user.getId_user();
        
        Object itemIngredient = IngredientComboBox.getSelectedItem();
        
        String ingredientName = ((String)itemIngredient);
        Ingredient ingredient_D = IngredientDAO.find(ingredientName);
        int ingredient_id= ingredient_D.getId_ingredient();
        
        
        Object itemPlate = plateComboBox.getSelectedItem();
        String PlateName = ((String)itemPlate);
        Plate plate_D = PlateDAO.find(PlateName);
        int IDPlate = plate_D.getPlateID();
                
               
        Object itemExercise = ExerciseComboBox.getSelectedItem();
        String ExerciseName = ((String)itemExercise);
        Exercise exercise_D = ExerciseDAO.find(ExerciseName);
        int exercise_id = exercise_D.getExerciseID();
        
        Object item = TypeComboBox.getSelectedItem();
        String name = ((String)item);
        
        String type=name;
        
         
         favorite.setDate(date);
         favorite.setType(type);
         favorite.setUser_id(user_id);
         favorite.setIngredient_id(ingredient_id);
         favorite.setPlate_id(IDPlate);
         favorite.setExercise_id(exercise_id);
         favorite.setId_favorite(id_favorite);
               
         if (FavoriteDAO.create(favorite)>0){
                  this.setVisible(false);
        new HomeView(current_user).setVisible(true);
        }
    }//GEN-LAST:event_saveFavoriteButtonActionPerformed

