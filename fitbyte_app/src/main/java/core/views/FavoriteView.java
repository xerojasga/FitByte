    private void revisarBTMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_revisarBTMActionPerformed
        ArrayList<Favorite> favs = FavoriteDAO.findAll();
        ArrayList<Favorite> favorites = new ArrayList<>();
        for (Favorite c: favs){
            if (favType().equals(c.getType()) && c.getUser_id() == current_user.getId_user()){
                favorites.add(c);
            }
        }
        for (Favorite c: favorites){
            String ingredient = IngredientDAO.find(c.getIngredient_id()).getName();
            String plate = PlateDAO.find(c.getPlate_id()).getPlateName();
            String exercise = ExerciseDAO.find(c.getExercise_id()).getExerciseName();
            Object[] data = new Object[]{ingredient, plate, exercise};
            model.addRow(data);
        }
        favoriteTable.setModel(model);
    }//GEN-LAST:event_revisarBTMActionPerformed

