    private ArrayList<String> typeSet(){
        ArrayList<String> d = new ArrayList<String>();
            String[] days = {"Desayuno", "Almuerzo", "Cena"};
            for (int i = 0; i < days.length; i++){
                d.add(days[i]);
        }
        return d;
    }
