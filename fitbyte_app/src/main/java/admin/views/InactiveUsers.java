    private void chargeBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chargeBTNActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        
        String d = dateTF.getText();
        Date date = Date.valueOf(d);
            
            ArrayList <Object> columns = new ArrayList<Object>();
            columns.add("IDENTIFIER");
            columns.add("USERNAME");
            columns.add("NAME");
            columns.add("LAST NAME");
            columns.add("EMAIL");
            columns.add("PASSWORD");
            columns.add("LAST LOGIN");
            columns.add("TYPE");
            for(Object column :columns){
                model.addColumn(column);
            }
            
            ArrayList<User> inactiveUsers = UserDAO.findByDate(date);
            for(User u:inactiveUsers){
                Object[] data = new Object[]{u.getId_user(),u.getUsername(),u.getName(),u.getLast_name(),u.getEmail(),u.getPassword(),u.getLast_login(),u.getType()};
                model.addRow(data);
            }
            UsersTBL.setModel(model);
    }//GEN-LAST:event_chargeBTNActionPerformed

